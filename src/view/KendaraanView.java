    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try{
            inputKosongException();
            idErrorException();
            jenisErrorException();
            notNumberException();
            
            
            mobilButton.setActionCommand("Mobil");
            trukButton.setActionCommand("Truk");
            
            Kendaraan d = new Kendaraan();
            
            if(jenisButtonGroup.getSelection().getActionCommand().compareTo("Mobil")==0){
                
                if(penumpangInput.getText().isEmpty()){
                    throw new InputKosongException();
                }
                try{
                    Integer.parseInt(penumpangInput.getText());
                    d = new Kendaraan(idInput.getText(), modelInput.getText(), jenisButtonGroup.getSelection().getActionCommand(), Integer.valueOf(tahunPembuatanInput.getText()), nomorPlatInput.getText(), Integer.valueOf(penumpangInput.getText()), 0);    
                }catch(NumberFormatException e){
                    throw new NotNumberException();
                }
                
                
            }else{
                if(dayaAngkutInput.getText().isEmpty()){
                    throw new InputKosongException();
                }
                
                try{
                    Integer.parseInt(dayaAngkutInput.getText());
                    d = new Kendaraan(idInput.getText(), modelInput.getText(), jenisButtonGroup.getSelection().getActionCommand(), Integer.valueOf(tahunPembuatanInput.getText()), nomorPlatInput.getText(), 0, Float.valueOf(dayaAngkutInput.getText()));        
                }catch(NumberFormatException e){
                    throw new NotNumberException();
                }
                
            }

            if(action.equals("Tambah")){
                kendaraanControl.insertDataKendaraan(d);
            }else{
                kendaraanControl.updateDataKendaraan(d, idInput.getText());
            }
            clearText();
            showKendaraan();
            setComponent(false);
            setEditDeleteBtn(false);
        }catch(InputKosongException e){
            JOptionPane.showMessageDialog(this, e.message());
        }catch(IdErrorException e){
            JOptionPane.showMessageDialog(this, e.message());
        }catch(JenisErrorException e){
            JOptionPane.showMessageDialog(this, e.message());
        }catch(NotNumberException e){
            JOptionPane.showMessageDialog(this, e.message());
        }
    }//GEN-LAST:event_saveBtnActionPerformed

