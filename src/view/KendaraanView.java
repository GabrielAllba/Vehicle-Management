    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        setEditDeleteBtn(true);
        setComponent(false);

        try{
            Kendaraan kendaraan = kendaraanControl.searchKendaraan(searchInput.getText());

            if(kendaraan == null){
                clearText();
                setEditDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Data tidak ditemukan", "Konfirmasi", JOptionPane.DEFAULT_OPTION);
            }else{
                
                idInput.setText(kendaraan.getId());
                modelInput.setText(kendaraan.getModel());
                
                if(kendaraan.getDayaAngkut()!=0){
                    dayaAngkutInput.setText(String.valueOf(kendaraan.getDayaAngkut()));
                    trukButton.setSelected(true);
                    
                }

                tahunPembuatanInput.setText(String.valueOf(kendaraan.getTahunPembuatan()));
                nomorPlatInput.setText(kendaraan.getNoPlat());
                

            }
        }catch(Exception e){
            System.out.println("Error : "+e.getMessage());
        }

    }//GEN-LAST:event_searchBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int getAnswer = JOptionPane.showConfirmDialog(rootPane, "Apakah yakin ingin menghapus data ? ", "Konfirmasi", JOptionPane.YES_NO_OPTION);

        if(getAnswer == JOptionPane.YES_OPTION){
            try{
                kendaraanControl.deleteDataKendaraan(idInput.getText());
                clearText();
                showKendaraan();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
            }catch(Exception e){
                System.out.println("Error : "+e.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Data tidak jadi dihapus!");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

